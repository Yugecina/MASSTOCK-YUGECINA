/**
 * Get activity icon by action name
 * @param action - Activity action string
 * @returns React element for the icon
 */
export function getActivityIcon(action: string): ReactElement {
  const iconMap: Record<string, () => ReactElement> = {
    'user_created_by_admin': UserCreatedIcon,
    'user_updated': UserUpdatedIcon,
    'user_deleted': UserDeletedIcon,
    'user_login': LoginIcon,
    'client_created': ClientCreatedIcon,
    'workflow_executed': WorkflowExecutedIcon,
    'workflow_created': WorkflowCreatedIcon,
    'workflow_failed': WorkflowFailedIcon,
    'ticket_created': TicketCreatedIcon
  };

  const IconComponent = iconMap[action] || DefaultActivityIcon;
  return IconComponent();
}
